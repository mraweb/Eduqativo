            <h1>Quem Somos</h1>

            <p>O Eduqativo - Instituto Choque Cultural é uma organização social sem fins lucrativos com a missão de tornar a arte mais acessível às novas gerações. Essa proposta educativa apoia-se no inovador conceito da Educomunicação aplicado à arte, onde a educação é feita através da comunicação com um diálogo aberto para promover novos ambientes de aprendizagem mais criativos e democráticos.</p>

            <p>O Eduqativo surgiu em 2011 como um departamento educativo da Choque Cultural para dar maior visibilidade e intencionalidade às suas intensas ações educacionais e públicas, assim como viabilizar seus projetos educativos e culturais de forma mais abrangente, iniciando parcerias com organizações educativas, culturais e artísticas, governamentais e da sociedade civil.</p>

            <p>Em 2012 o Eduqativo constituiu-se juridicamente, compartilhando da mesma visão de um futuro com mais arte e educação para todos idealizado pela Editora e Galeria Choque Cultural, fundada em 2003 por Mariana Martins, Baixo Ribeiro e Eduardo Saretta.</p>

            <p>Aliando-se à experiência de 10 anos da Choque Cultural no campo da Arte e Cultura, o Eduqativo mantém um programa focado na qualificação profissional de artistas e professores, na formação de novos públicos e no desenvolvimento da arte pública. E têm promovido uma importante discussão sobre a permeabilidade dos ambientes da arte, do mercado, da escola e outras instituições através da realização de exposições e eventos artísticos experimentais em seus espaços próprios, em museus e no espaço público.</p>

