            <div id="accordion">
                  <h2 class="bg-tit"><a href="#" title="Incentivo Fiscal - Doação Pessoa Física (IR)" class="link">Incentivo Fiscal - Doação Pessoa Física (IR)</a></h2>

                  <div>
                        <p><strong>Incentivo à Cultural<br />
                        O que é o benefício fiscal de incentivo a cultura?</strong></p>

                        <p>Com o objetivo de incentivar as atividades culturais, a União faculta às pessoas físicas a opção pela aplicação de parcelas do Imposto sobre a Renda a título de doações, para apoio direto a projetos culturais apresentados por pessoas físicas ou por pessoas jurídicas de natureza cultural, de caráter privado.</p>

                        <p>Os contribuintes poderão deduzir do imposto de renda devido as quantias efetivamente despendidas nos projetos previamente aprovados pelo Ministério da Cultura, nos limites e condições estabelecidos na legislação do imposto de renda vigente, na forma de doações e patrocínios.</p>
                        
                        <p>Dessa forma, nos termos do artigo 19 da Lei Rouanet - Lei Federal de Incentivo à Cultura, os:</p>

                        <p><strong>Projeto Linguagens Artísticas PRONAC 127358<br />
                        Projeto Biblioteqas Choque PRONAC 129338</strong></p>

                        <p>Aprovados pelo Ministério da Cultura, permitem que pessoas físicas direcionem parte do seu Imposto de Renda devido para manutenção destes projetos culturais.</p>
                
                        <p><strong>Como utilizar o benefício?</strong></p>

                        <p>O valor da doação poderá ser deduzido na declaração de ajuste anual do exercício financeiro subseqüente ao da doação. Para pessoas físicas, o limite de dedução é de 6% do imposto apurado. Para o gozo deste benefício o doador deverá utilizar o modelo completo de declaração do IRPF.</p>
                        
                        <p>O valor que ultrapassar o limite de dedutibilidade mencionado não pode ser deduzido nas declarações posteriores, inclusive no caso de projetos culturais de execução plurianual.</p>

                        <p><strong>Como é feita a dedução?</strong></p>

                        <p>No programa fornecido pela Secretaria da Receita Federal, o contribuinte deverá optar pelo formulário completo de declaração de ajuste anual do imposto de renda e informar, no campo "Pagamentos e Doações Efetuados", código 41 (Incentivo a Cultura), o valor e os dados que constam no recibo de mecenato fornecido pelo Eduqativo-Instituto Choque Cultural. As doações efetuadas poderão ser deduzidas até o limite de 6% do imposto apurado, considerando o somatório das deduções relativas ao Incentivo à Cultura e outras deduções legais.</p>
                        
                        <p><strong>Como fazer a doação?</strong></p>

                        <p>Os recursos serão depositados nas Contas Captação.</p>

                        <p><strong>Projeto Linguagens Artísticas </strong><span>(Banco do Brasil S.A. - Agência, C/C, Titular: Eduqativo-Instituto Choque Cultural, CNPJ 15.335.222/0001-32)</span></p>

                        <p><strong>Projeto Biblioteqas Choque</strong> <span>(Banco do Brasil S.A. - Agência, C/C, Titular: Eduqativo-Instituto Choque Cultural, CNPJ 15.335.222/0001-32)</span></p>
                        
                        <p>Por meio de depósito identificado, com a informação obrigatória do CPF do depositante, ou, alternativamente, por Transferência Eletrônica Disponível - TED, ou Documento de Operação de Crédito- DOC. Correntistas do Banco do Brasil devem usar a opção depósito identificado, informando o número do CPF no campo identificador 1.</p>

                        <p>A partir da confirmação do depósito, o Eduqativo-Instituto Choque Cultural emitirá o recibo de mecenato que será enviado ao doador para as devidas comprovações junto à Receita Federal. O doador deverá guardar este recibo à disposição da Secretaria da Receita Federal do Brasil.</p>    
                         
                        <p><strong>Vedações</strong></p>

                        <p>A dedução fiscal não poderá ser realizada para doações realizadas por dirigentes do Eduqativo-Instituto Choque Cultural, bem como de empresas associadas ao Instituto, e seus parentes até terceiro grau, nos termos do art. 27 da Lei Rouanet.</p> 
